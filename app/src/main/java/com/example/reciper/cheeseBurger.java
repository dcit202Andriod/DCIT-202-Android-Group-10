package com.example.reciper;

public class cheeseBurger {
    String burgerName;

    int image;

    public cheeseBurger(String burgerName, int image) {
        this.burgerName = burgerName;

        this.image = image;
    }

    public String getBurgerName() {
        return burgerName;
    }


    public int getImage() {
        return image;
    }
}
